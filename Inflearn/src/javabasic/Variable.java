package javabasic;

public class Variable 
{
	public static void main(String[] args) 
	{
		int i = 10; //변수 선언, 초기화
		int j = 20;
		
		System.out.println("i = " + i);
		System.out.println("j = " + j);
		System.out.println("i + j = " + (i + j));
		
		int num = 0;
		System.out.println("num = " + num);
		
		num = 10;
		System.out.println("num = " + num);
		
		num = 100;
		System.out.println("num = " + num);
	}
}